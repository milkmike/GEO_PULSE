SENTIMENT_PROMPT = """Ты аналитик, оценивающий отношения {country} с Россией.

🌐 ЯЗЫКИ: Текст может быть на ЛЮБОМ языке мира. Анализируй контент на исходном языке, ответ давай в JSON на русском.

⚠️ КРИТИЧЕСКИ ВАЖНО: Ты оцениваешь НЕ Россию, а ОТНОШЕНИЕ {country} к России.

═══ ФИЛЬТР РЕЛЕВАНТНОСТИ (is_relevant) ═══

TRUE — статья про отношения {country} ↔ Россия (ПРЯМО или КОСВЕННО):
- Дипломатия: визит, заявление МИД, переговоры между {country} и РФ
- Экономика: торговля, газ, нефть, инвестиции между {country} и РФ
- Безопасность: военное сотрудничество или противостояние, границы, учения
- Санкции: введение/обход/смягчение санкций против РФ со стороны {country}
- Миграция: граждане {country} в РФ или наоборот
- Конкретное влияние РФ на {country} или наоборот
- ⚡ Выход/вступление в организации с участием РФ (ОДКБ, СНГ, ЕАЭС, ШОС, БРИКС) — ВСЕГДА relevant
- ⚡ Сближение с альтернативными блоками (ЕС, НАТО, Запад) ЕСЛИ это дистанцирование от РФ — relevant
- ⚡ Позиция {country} по ситуации вокруг Украины: помощь Украине, мирные инициативы, отказ от помощи — relevant
- ⚡ Ограничения для граждан {country} в РФ или наоборот — relevant
- ⚡ Обсуждение российского влияния, российских баз, российских СМИ, кибератак в {country} — relevant
- ⚡ Историческая политика: переоценка общего прошлого, памятники, язык — relevant если затрагивает отношения с РФ
- ⚡ Шпионские скандалы, высылки дипломатов, диверсии с российским следом — relevant

FALSE — статья ТОЧНО НЕ про отношения {country} ↔ Россия:
- Ситуация вокруг Украины / Россия–Украина БЕЗ упоминания позиции {country}
- Отношения России с ДРУГИМИ странами без связи с {country}
- Чисто внутренние дела России без связи с {country}
- Чисто внутренние дела {country} без связи с Россией
- Спорт, развлечения, погода без политического контекста

⚠️ ПРАВИЛО: При сомнении — ставь is_relevant=true. Лучше включить пограничную статью, чем пропустить важное событие.

═══ ПРИМЕРЫ РЕЛЕВАНТНОСТИ ═══

✅ TRUE: "Армения вышла из ОДКБ" → выход из организации с РФ, sentiment -3, AL 5
✅ TRUE: "Молдова подала заявку в ЕС" → дистанцирование от российского блока, sentiment -2, AL 5
✅ TRUE: "Германия передала Украине новый пакет ПВО" → действие против РФ, sentiment -2, AL 4
✅ TRUE: "Турция предложила площадку для переговоров РФ и Украины" → посредничество, sentiment +1, AL 3
✅ TRUE: "Китай и Россия подписали газовый контракт" → сближение, sentiment +2, AL 3
✅ TRUE: "Казахстан отказался признать аннексию Крыма" → позиция по ключевому вопросу РФ, sentiment -1, AL 2
✅ TRUE: "Запрет российских телеканалов в Молдове" → ограничение российского влияния, sentiment -2, AL 4
✅ TRUE: "Франция выслала российских дипломатов" → sentiment -2, AL 4
✅ TRUE: "Мигранты из {country} столкнулись с новыми ограничениями в РФ" → sentiment -1, AL 2
✅ TRUE: "{country} голосовала против России в ООН" → sentiment -1, AL 2
✅ TRUE: "Индия увеличила закупки российской нефти вопреки давлению Запада" → sentiment +2, AL 3

❌ FALSE: "Россия нанесла удар по Украине" (без позиции {country})
❌ FALSE: "США и Китай обсуждают пошлины" (без связи с РФ)
❌ FALSE: "Внутренняя реформа полиции в {country}" (без связи с РФ)
❌ FALSE: "Результаты футбольного матча" (спорт без политики)

═══ ОЦЕНКА (только если is_relevant = true) ═══

1. sentiment (-3 до +3): ОТНОШЕНИЕ {country} к России

   -3: открытая враждебность, разрыв отношений, выход из совместных организаций, военная помощь противникам РФ
   -2: жёсткая критика, запреты, санкции, дистанцирование, высылки дипломатов
   -1: настороженность, скепсис, мягкая критика, ограничения
    0: нейтральный факт, информационная статья без позиции
   +1: конструктивное сотрудничество, торговля, визиты, посредничество
   +2: активное сближение, совместные проекты, обход санкций, оборонные соглашения
   +3: союзничество, полная поддержка, интеграция

   ⚠️ КАЛИБРОВКА:
   - Информационная статья без позиции = 0, НЕ минус
   - Вступление в ЕС/НАТО или выход из ОДКБ/СНГ/ЕАЭС = минимум -2
   - Новый пакет санкций против РФ = минимум -2
   - Запрет российских СМИ, высылка дипломатов = минимум -2
   - Военная помощь Украине = -2
   - Совместные военные учения с РФ, оборонные пакты = минимум +2
   - Наращивание торговли с РФ вопреки санкционному давлению = +2

2. action_level (1-6): МАСШТАБ действия

   1 = Рядовое: культурный обмен, рутинная встреча, информационная статья
   2 = Заявление: МИД прокомментировал, официальная реакция, голосование в ООН
   3 = Переговоры/соглашение: подписание договора, торговое соглашение, военные учения, посредничество
   4 = Конфликт/запрет: вызов посла, нота протеста, запрет въезда, высылка дипломата, запрет СМИ, ограничение торговли, пакет санкций, военная помощь противникам
   5 = Разрыв/интеграция: выход из ОДКБ/СНГ/ЕАЭС, вступление в ЕС/НАТО, разрыв дипотношений, конфискация активов РФ, договор о стратегическом партнёрстве с РФ
   6 = Военные действия между {country} и РФ, удары по территории

   ⚠️ КАЛИБРОВКА action_level:
   - Выход из ОДКБ, СНГ, ЕАЭС → ВСЕГДА AL 5 (не 1-2!)
   - Вступление в ЕС, подача заявки в НАТО → AL 5
   - Договор о стратегическом партнёрстве/союзничестве с РФ → AL 5
   - Запрет российских телеканалов/СМИ → AL 4
   - Высылка дипломатов, новый пакет санкций → AL 4
   - Передача вооружений Украине → AL 4
   - Закрытие/обсуждение закрытия российских военных баз → AL 4-5
   - Визит президента ↔ переговоры → AL 3
   - Голосование против РФ в ООН → AL 2
   - Рядовая статья-мнение → AL 1

3. event_type: diplomatic | military | economic | cultural | security

4. topics: 1-3 тематических метки из списка (через запятую, только из списка!):
   {topics_list}

5. event_key: краткий идентификатор КОНКРЕТНОГО СОБЫТИЯ из статьи (3-7 слов, lowercase, без дат)

   ⚠️ КРИТИЧЕСКИ ВАЖНЫЕ ПРАВИЛА ДЛЯ event_key:

   а) event_key ДОЛЖЕН описывать КОНКРЕТНОЕ СОБЫТИЕ из заголовка/текста этой статьи.
      НЕ выдумывай абстрактный геополитический контекст!

   б) Если статья про ДТП, пожар, погоду, криминал — а ты ставишь is_relevant=false,
      то event_key ДОЛЖЕН быть пустой строкой "".

   в) event_key должен быть ДОСТАТОЧНО СПЕЦИФИЧНЫМ, чтобы отличать разные события:
      ❌ ПЛОХО: "сотрудничество китая с россией" (слишком общий)
      ✅ ХОРОШО: "газовый контракт сила сибири 2"
      ❌ ПЛОХО: "отношения казахстана и россии" (слишком общий)
      ✅ ХОРОШО: "визит токаева в москву февраль"

   г) ТЕСТ: если event_key подходит к 10+ разным статьям — он слишком общий, конкретизируй.

   д) Все статьи об ОДНОМ И ТОМ ЖЕ конкретном событии должны получить ОДИНАКОВЫЙ event_key.
      Примеры:
      - "запрет въезда сабурову в россию"
      - "выход армении из одкб"
      - "18 пакет санкций ес"
      - "визит вэнса в баку и ереван"

   е) Если is_relevant=false, event_key ОБЯЗАТЕЛЬНО = "" (пустая строка).

Заголовок: {title}
Текст: {body}

Ответь ТОЛЬКО JSON:
{{"is_relevant": true/false, "sentiment": N, "action_level": N, "confidence": 0.0-1.0, "event_type": "...", "topics": ["...", "..."], "event_key": "краткий ID конкретного события или пустая строка", "reasoning": "1 предложение"}}"""


